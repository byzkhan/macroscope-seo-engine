class OutlineSection(BaseModel):
    """A section in the article outline."""

    heading: str = Field(..., min_length=2)
    description: str = Field(..., min_length=5)
    target_word_count: int = Field(default=200, ge=50, le=1500)
    key_points: list[str] = Field(default_factory=list)


class FAQ(BaseModel):
    """A frequently asked question with suggested answer."""

    question: str = Field(..., min_length=10)
    suggested_answer: str = Field(..., min_length=20)


class InternalLink(BaseModel):
    """A suggested internal link to existing Macroscope content."""

    anchor_text: str
    target_path: str = Field(..., description="Relative URL path on macroscope.com")
    context: str = Field(..., description="Where/how to place this link in the article")


class ResearchBrief(BaseModel):
    """Comprehensive research brief for blog writing.

    Contains everything a writer needs to produce the article
    without additional research.
    """

    topic: TopicCandidate
    outline: list[OutlineSection] = Field(..., min_length=3)
    target_word_count: int = Field(default=2000, ge=800, le=5000)
    primary_keyword: str
    secondary_keywords: list[str] = Field(..., min_length=2)
    entities: list[str] = Field(..., min_length=1, description="People, companies, tools, concepts")
    faqs: list[FAQ] = Field(..., min_length=4)
    claims_needing_evidence: list[str]
    internal_link_suggestions: list[InternalLink]
    cta: str = Field(..., min_length=5)
    do_not_say: list[str] = Field(..., min_length=1)
    meta_description: str = Field(..., min_length=50, max_length=160)
    title_options: list[str] = Field(..., min_length=2)

    @field_validator("primary_keyword")
    @classmethod
    def keyword_normalized(cls, v: str) -> str:
        return v.lower().strip()

    def brief_hash(self) -> str:
        """SHA-256 prefix for traceability."""
        return hashlib.sha256(self.model_dump_json().encode()).hexdigest()[:12]


