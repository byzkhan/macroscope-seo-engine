    stages: list[StageResult] = Field(default_factory=list)
    topic_selected: Optional[str] = None
    final_score: Optional[float] = None
    final_grade: Optional[str] = None
    word_count: Optional[int] = None
    artifacts: dict[str, str] = Field(
        default_factory=dict, description="stage_name -> artifact file path"
    )
    errors: list[str] = Field(default_factory=list)

    @computed_field
    @property
    def success(self) -> bool:
        """Whether the run completed without errors."""
        return len(self.errors) == 0 and self.completed_at is not None

    @computed_field
    @property
    def total_duration_seconds(self) -> float:
        """Total wall-clock time of the run."""
        if self.started_at and self.completed_at:
            return round((self.completed_at - self.started_at).total_seconds(), 2)
        return sum(s.duration_seconds for s in self.stages)

    @computed_field
    @property
    def stages_completed(self) -> list[str]:
        """Names of stages that completed successfully."""
        return [s.stage for s in self.stages if s.success]

    @computed_field
    @property
    def stages_failed(self) -> list[str]:
        """Names of stages that failed."""
        return [s.stage for s in self.stages if not s.success]

    def to_concise_json(self) -> dict:
        """Return a CI-friendly concise summary dict."""
        return {
            "run_id": self.run_id,
            "success": self.success,
            "topic": self.topic_selected,
            "score": self.final_score,
            "grade": self.final_grade,
            "word_count": self.word_count,
            "duration_seconds": self.total_duration_seconds,
            "resume_count": self.resume_count,
            "resumed_from_stage": self.resumed_from_stage,
            "stages_completed": self.stages_completed,
            "stages_failed": self.stages_failed,
            "error_count": len(self.errors),
            "errors": self.errors[:5],
        }
