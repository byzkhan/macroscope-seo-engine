class TopicCandidate(BaseModel):
    """A proposed blog topic generated by the topic researcher."""

    title: str = Field(..., min_length=10, max_length=200)
    slug: str = Field(..., pattern=r"^[a-z0-9]+(?:-[a-z0-9]+)*$")
    cluster: str = Field(..., description="Topic cluster from topic_clusters.yaml")
    description: str = Field(..., min_length=20)
    target_keywords: list[str] = Field(..., min_length=2)
    search_intent: SearchIntent
    freshness_signal: Optional[str] = None
    source: str = Field(..., description="Agent or signal that generated this candidate")
    rationale: str = Field(..., min_length=10)

    @field_validator("target_keywords")
    @classmethod
    def keywords_are_lowercase(cls, v: list[str]) -> list[str]:
        return [kw.lower().strip() for kw in v]

    @field_validator("slug")
    @classmethod
    def slug_is_normalized(cls, v: str) -> str:
        return v.lower().strip()


