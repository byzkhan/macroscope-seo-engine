class MarketSignalReport(BaseModel):
    """Aggregated market intelligence report."""

    signals: list[MarketSignal]
    trending_themes: list[str]
    recommended_angles: list[str]
    collected_at: datetime = Field(default_factory=lambda: datetime.now(timezone.utc))

    @field_validator("trending_themes")
    @classmethod
    def dedupe_themes(cls, v: list[str]) -> list[str]:
        seen: set[str] = set()
        result: list[str] = []
        for t in v:
            key = t.lower().strip()
            if key not in seen:
                seen.add(key)
                result.append(t)
        return result


