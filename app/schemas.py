class TopicScore(BaseModel):
    """Weighted scoring for a topic candidate.

    Weights: business_relevance=25, search_opportunity=20, aeo_fit=15,
    freshness=10, authority_to_win=15, uniqueness_vs_archive=10, production_ease=5.
    Total possible: 100.
    """

    business_relevance: float = Field(..., ge=0.0, le=25.0)
    search_opportunity: float = Field(..., ge=0.0, le=20.0)
    aeo_fit: float = Field(..., ge=0.0, le=15.0)
    freshness: float = Field(..., ge=0.0, le=10.0)
    authority_to_win: float = Field(..., ge=0.0, le=15.0)
    uniqueness_vs_archive: float = Field(..., ge=0.0, le=10.0)
    production_ease: float = Field(..., ge=0.0, le=5.0)

    @computed_field
    @property
    def total(self) -> float:
        """Sum of all scoring dimensions."""
        return round(
            self.business_relevance
            + self.search_opportunity
            + self.aeo_fit
            + self.freshness
            + self.authority_to_win
            + self.uniqueness_vs_archive
            + self.production_ease,
            2,
        )


class ScoredTopic(BaseModel):
    """A topic candidate with its score and selection status."""

    candidate: TopicCandidate
    score: TopicScore
    rejection_reasons: list[str] = Field(default_factory=list)
    selected: bool = False

    @computed_field
    @property
    def total_score(self) -> float:
        """Convenience accessor for the topic's total score."""
        return self.score.total


