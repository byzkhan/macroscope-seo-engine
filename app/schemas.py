"""Pydantic models for the Macroscope SEO content pipeline.

These schemas define the data contracts between all pipeline stages.
Every agent input and output must conform to these models.
"""

from __future__ import annotations

import hashlib
from datetime import datetime, timezone
from enum import Enum
from typing import Annotated, Optional

from pydantic import BaseModel, Field, computed_field, field_validator


class SearchIntent(str, Enum):
    """Classification of user search intent."""

    INFORMATIONAL = "informational"
    NAVIGATIONAL = "navigational"
    TRANSACTIONAL = "transactional"
    COMMERCIAL = "commercial"


class PipelineStage(str, Enum):
    """Named pipeline stages for tracking and artifact mapping."""

    COLLECT_SIGNALS = "collect_signals"
    GENERATE_TOPICS = "generate_topics"
    SCORE_TOPICS = "score_topics"
    BUILD_BRIEF = "build_brief"
    WRITE_DRAFT = "write_draft"
    QA_OPTIMIZE = "qa_optimize"
    EXPORT = "export"
    PERSIST_HISTORY = "persist_history"


class FailureCategory(str, Enum):
    """Categorized failure types for structured error handling."""

    VALIDATION_ERROR = "validation_error"
    PROVIDER_ERROR = "provider_error"
    SCORING_REJECTION = "scoring_rejection"
    QA_FAILURE = "qa_failure"
    IO_ERROR = "io_error"
    TIMEOUT = "timeout"
    UNKNOWN = "unknown"


# ---------------------------------------------------------------------------
# Topic candidates
# ---------------------------------------------------------------------------


class TopicCandidate(BaseModel):
    """A proposed blog topic generated by the topic researcher."""

    title: str = Field(..., min_length=10, max_length=200)
    slug: str = Field(..., pattern=r"^[a-z0-9]+(?:-[a-z0-9]+)*$")
    cluster: str = Field(..., description="Topic cluster from topic_clusters.yaml")
    description: str = Field(..., min_length=20)
    target_keywords: list[str] = Field(..., min_length=2)
    search_intent: SearchIntent
    freshness_signal: Optional[str] = None
    source: str = Field(..., description="Agent or signal that generated this candidate")
    rationale: str = Field(..., min_length=10)

    @field_validator("target_keywords")
    @classmethod
    def keywords_are_lowercase(cls, v: list[str]) -> list[str]:
        return [kw.lower().strip() for kw in v]

    @field_validator("slug")
    @classmethod
    def slug_is_normalized(cls, v: str) -> str:
        return v.lower().strip()


# ---------------------------------------------------------------------------
# Market signals
# ---------------------------------------------------------------------------


class MarketSignal(BaseModel):
    """A single signal from market/social/news monitoring."""

    source: str
    title: str
    url: Optional[str] = None
    summary: str = Field(..., min_length=10)
    relevance_score: float = Field(..., ge=0.0, le=1.0)
    detected_at: datetime
    themes: list[str] = Field(..., min_length=1)


class MarketSignalReport(BaseModel):
    """Aggregated market intelligence report."""

    signals: list[MarketSignal]
    trending_themes: list[str]
    recommended_angles: list[str]
    collected_at: datetime = Field(default_factory=lambda: datetime.now(timezone.utc))

    @field_validator("trending_themes")
    @classmethod
    def dedupe_themes(cls, v: list[str]) -> list[str]:
        seen: set[str] = set()
        result: list[str] = []
        for t in v:
            key = t.lower().strip()
            if key not in seen:
                seen.add(key)
                result.append(t)
        return result


