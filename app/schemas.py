class DraftArticle(BaseModel):
    """First draft of the blog article."""

    title: str
    slug: str = Field(..., pattern=r"^[a-z0-9]+(?:-[a-z0-9]+)*$")
    content_md: str = Field(..., min_length=100)
    word_count: int = Field(..., ge=1)
    brief_hash: str = Field(..., description="SHA-256 prefix of the serialized brief")
    created_at: datetime = Field(default_factory=lambda: datetime.now(timezone.utc))


class SEOAEOScore(BaseModel):
    """SEO and AEO quality scores (each dimension 0-10, total 0-100)."""

    title_score: float = Field(..., ge=0.0, le=10.0)
    meta_description_score: float = Field(..., ge=0.0, le=10.0)
    keyword_density_score: float = Field(..., ge=0.0, le=10.0)
    heading_structure_score: float = Field(..., ge=0.0, le=10.0)
    internal_links_score: float = Field(..., ge=0.0, le=10.0)
    faq_presence_score: float = Field(..., ge=0.0, le=10.0)
    direct_answer_score: float = Field(..., ge=0.0, le=10.0)
    readability_score: float = Field(..., ge=0.0, le=10.0)
    content_depth_score: float = Field(..., ge=0.0, le=10.0)
    freshness_signals_score: float = Field(..., ge=0.0, le=10.0)

    @computed_field
    @property
    def total(self) -> float:
        """Sum of all SEO/AEO dimensions (0-100)."""
        return round(
            self.title_score
            + self.meta_description_score
            + self.keyword_density_score
            + self.heading_structure_score
            + self.internal_links_score
            + self.faq_presence_score
            + self.direct_answer_score
            + self.readability_score
            + self.content_depth_score
            + self.freshness_signals_score,
            2,
        )

    @computed_field
    @property
    def normalized(self) -> float:
        """Normalized score (0.0-1.0)."""
        return round(self.total / 100.0, 4)

    @computed_field
    @property
    def grade(self) -> str:
        """Letter grade: A (>=80), B (>=65), C (>=50), D (>=35), F (<35)."""
        t = self.total
        if t >= 80:
            return "A"
        if t >= 65:
            return "B"
        if t >= 50:
            return "C"
        if t >= 35:
            return "D"
        return "F"


class FinalArticle(BaseModel):
    """The final, QA-passed article ready for publishing."""

    title: str
    slug: str = Field(..., pattern=r"^[a-z0-9]+(?:-[a-z0-9]+)*$")
    content_md: str = Field(..., min_length=100)
    meta_description: str = Field(..., min_length=50, max_length=160)
    word_count: int = Field(..., ge=1)
    seo_aeo_score: SEOAEOScore
    faqs_present: bool
    internal_links: list[str]
    created_at: datetime = Field(default_factory=lambda: datetime.now(timezone.utc))


